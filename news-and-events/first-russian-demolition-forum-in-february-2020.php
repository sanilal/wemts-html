											<p><strong>Similar to Demtech India 2019 Russia will organize its first demolition forum in 2020.</strong><br>

												The forum will take place at Korston Club Hotel in Moscow, Russia, 4-5 February 2020. Initiator to the forum is Artem Kondratev, General Manager of the Russian Demolition Federation. The form will be organized in partnership with EDA (European Demolition Association) and supported by PDi Magazine. 

</p>
											<p>The main goal of the forum is to create a platform for cooperation between Russian demolition companies, exchange of professional experience of modern technologies with authoritative European and World experts in demolition industry. Participant at the forum will be Russian demolition contractors, international demolition contractors, the management of EDA, NFDC (National Federation of Demolition Contractors, UK) SDA (Serbian Demolition Association, leading industrial companies and enterprises of Russia, global manufacturers of demolition attachments and machines, etc.

</p>
											<p>Some of the topics that will be focused on during the forum will be: Current trends in the demolition industry and the general movement of the market, modern innovation technologies for demolition and dismantling of buildings and constructions, European and World demolition experiences and job site stories with speakers form EDA, laureates of the international demolition awards, global demolition experts, waste management lectures and lectures of how to re-cultivate contaminated land, etc. Within the framework of the forum the first awards for Russian demolition projects will be given. Guests of honours, speakers and members of the awards judge panel will be General Secretary of the European Demolition Association (EDA) Jose Blanco (Spain), National Federation of Demolition Contractors (NFDC) CEO Howard Button, President of the EDA Francisco Cobo Valero (Spain), CEO of the DESPE demolition, EDA Vice â€“President Stefano Panseri (Italy), Managing Director of the RVA Group Richard Vann (UK), founder and Managing Director of Moltoni Corporation Robert Charles Moltoni (Australia), CEO of the DDM Group Theo Velis, Commercial Director of the DDM Group Jaques Fontaine (Netherlands), international trade manager of the Delete company Nikolas Ruotsalainen (Finland), President of the Serbian Demolition Association Dejan Bojovic (Serbia).

</p>
											<p>At the forum there will be an inauguration ceremony of the first Russian Demolition Association. A special education academy for he Russian demolition industry will also be set up at the forum.<br>


