											</ul>
											<p>Brokk, the manufacturer of remote controlled demolition machines, recently finalised an agreement with Brock Rentals Inc. to upgrade the rental company’s fleet of remote controlled demolition units with 12 next generation models.




</p>
											<p>The new models feature Brokk’s signature ‘SmartConcept’ technology, which revolutionises power management, design and ergonomics to maximise productivity over previous models. With the new models, Brock Rentals’ fleet now features more than 50 Brokk remote controlled machines ranging from the compact Brokk 60 to the heavy duty Brokk 400. 





</p>
											<p>“We’ve always relied on the safety and productivity of Brokk machines to provide viable solutions for our customers in refractory, chemical, nuclear, demolition and general construction industries,” said Tomas Lundstrom, president of Brock Rentals. “The next generation models offer the best power to weight ratio on the market, in some cases doubling the power over our current models with only a slight increase in weight and dimension, opening up new opportunities and applications for our clients. In addition, they are more dependable and easier to service, making them ideal for the rental industry where machines seldom come back to the shop without some signs of use.”



 



</p>
											<p>Brock Rentals opened in 1994, with one of its first machines being a Brokk 150 which was used mainly in refractory applications. As its customer base grew to include concrete demolition contractors with a wider range of applications, so did its fleet. Today the company provides rental machines to contractors and government agencies in the U.S., Canada and Mexico. Brock Rentals coordinates with certified Brokk operators across North America to ensure the safety and efficiency of its machines on every jobsite, as well as offer training and technical support to customers. 

</p>
											<p>As with all Brokk machines, the four new rental units feature Brokk’s three part arm for exceptional reach and movement, as well as industry leading power to weight ratios. “Brokk has always been the industry leader when it comes to technology, capabilities and raw power,” said Lars Lindgren, president of Brokk Inc. “We continue to upgrade our machines to meet the needs of an ever changing demolition industry. Working with companies like Brock Rentals allows us to reach new customers and see how our equipment performs in new applications, helping us continue to advance remote controlled demolition equipment with more efficient machines and attachments.”



</p>



