											<h4><a href="blog-details.html">65 year old silo demolished using Volvo’s new high reach demolition excavator</a></h4>
											<ul class="blog-meta">
												<li class="date"><i class="fa fa-calendar-check-o"></i> November 2019</li>
											</ul>
											<p>Älmby Entreprenad is somewhat of a hidden secret in the Swedish demolition industry, but is a significant player, with an annual turnover of around €34M and a fleet of about 30 different excavators. Now its become the first company in Sweden to dismantle an entire 34m silo using Volvo’s new high reach demolition excavator.






</p>
											<p>Älmby Entreprenad was commissioned by the Torsås municipality to demolish a 34m 65 year old silo. To do this the company used a custom built, 55t high reach Volvo EC380EHR demolition excavator, which levelled the silo in Bergkvara, Sweden, to the ground.


</p>
											<h4>Silos nothing but a memory

</h4>
											<p>The reason for the demolition was that the peak of silo storing seems to have passed, and the question often arises about what to do with them. Some want to keep them, as monuments of times past, others want to convert them for other purposes and some think the only reasonable thing is dismantling them. The concrete silo in question is located off the shore of Bergkvara in southeast Småland, was constructed in 1955 as a seed storage facility, and today is deemed to be in too poor condition to be rebuilt or renovated. After years of discussions, the local municipality decided to demolish the silo before transforming parts of the shore into residential areas.</p>
											<p>The Älmhult based company Älmby Entreprenad began the project in February, starting with the sanitation of the complete silo structure. “We have primarily sanitized the complete structure of PCB, asbestos and PAH,” says Jonny Nilsson, location manager and associate of Älmby Entreprenad AB, when PDi Magazine met him a couple of days before the demolition began. “The sanitation itself wasn’t too complicated, but carrying the sanitation equipment up the staircase tower’s 160 steps was quite the workout for the staff.” Following this, the initial stage of demolition began.

</p>
											<p>Adjacent to the silo was a separate storage facility that had been used for various purposes throughout the years, most recently as a machine room. For the demolition of this facility, the company used a Volvo EC300E equipped with a NPK U-21pulveriser, which immediately separated the concrete steel from the concrete. Tomas Fransson operated the excavator, ensuring that the silo was dismantled without problems together with Magnus Rostedt, who operated the smaller demolition excavator, a Volvo EC300E.

</p>

<p>The model used for the high reach demolition was launched by Volvo last year, and is one of three machines in Volvo’s high reach demolition excavator series. A 3m long extra boom gives it a total reach of 26m. The undercarriage can be widened for extra stability when the machine is working at great heights with a counterweight of 4t for greater stability, with the hydraulic concrete crusher used for the silo demolition being a 5t NPK s-16XCR. “This is a fun and exciting project. The challenge is the altitude and making sure no demolition debris falls into the ocean,” says Tomas Fransson.

</p>
<h4>Great local interest</h4>
											<p>The demolishing of the silo has been a hot topic in the municipality for many years and the locals have been curious to watch the work progress. “We don’t let ourselves get stressed out by it. We do our job and take it easy. It’s just fun to see that there’s an interest in the demolishing. As we understand it, the majority of the population in the municipality are happy we’re demolishing the silo,” says Tomas.</p>
											<p>Tomas and Magnus have both been employed by Älmby Entreprenad for six years. Tomas has been focused on demolition and is the most experienced in the company when it comes to working with the high reach excavator. Magnus started working in demolition one year ago and since then the colleagues have worked together on a couple of projects, often working far away from home and living together in a van. “Tomas and I work well together and that’s a big plus when you’re colleagues and live so close together,” says Magnus of the good relationship between the two men. Furthermore, there is usually little spare time in the evenings. “We work from six in the morning to six in the evening Monday to Wednesday, so that we don’t need to work for the half of Thursday and the whole of Friday. It’s better to just go on when you’re out on a job, so that we can come back home in time on Thursday,” says Magnus.</p>
											<p>Älmby’s initial plan was that as the towers were demolished little by little, from the outside moving inwards towards the taller 34m main structure, a ramp would be built from the resulting debris, up towards the main structure. The total height of the ramp had to be 8m in order for the excavator to reach the top. As the demolition progressed, it was realised that there wasn’t enough demolition rubble for such a high ramp. This meant bringing in a remote controlled demolition robot, a Husqvarna DXR 310, which was lifted up to the roof of the building. It worked its way down 2m using both a hydraulic hammer and a crusher. This resulted in enough concrete rubble to build a 6m ramp enabling the high reach excavator to take over and dismantle the remaining 32m of the structure.

</p>
											<p>Tomas and Magnus were both conscious of the risk that not everything would progress smoothly, but both men are oriented towards problem solving, viewing it as one of the perks of the job. At an early stage, a challenge arose, and Älmby was a little unsure how to proceed when it came to tearing down a large iron beam located on the roof on the upper building on the towers. It turned out to be completely stuck, but eventually it was removed in one piece. Another unexpected problem was the breaking of boom, which had to be replaced from overseas, which took two weeks. Despite this, the demolition progressed well, taking around five weeks before the silo in Bergkvara was completely demolished.

</p>
											<p>Today when visiting the Bergkvara shore, the only thing that remains of the silo are piles of concrete of what had been a part of the village for 65 years. Of the 5,000t of the resulting demolition debris, 4,000t was transported just a couple of miles away to be used for a construction job, whilst the remaining debris was disposed of as waste.
</p>
											<p>Project information on the silo demolition in Bergkvara:

</p>
											<p><strong>Silo building</strong>: Constructed in1955 as a depot and for the interim storage of seed<br>
<strong>Contractor</strong>: Älmby Entreprenad AB from Älmhult, Sweden<br>

<strong>Client</strong>: Torsås municipality<br>

<strong>Highest point</strong>: 34m<br>

<strong>Included in the demolition project</strong>: A storehouse building as well as the silo with 15 towers and one main building<br>

<strong>Concrete mass</strong>: 5,000t<br>

<strong>Steel</strong>: ca 100t–200t<br>

<strong>Equipment used</strong>: One Volvo 55t high reach demolition excavator, EC380EHR, with a boom reach of 26m reach, plus a 30t Volvo EC300E<br>

<strong>Project partner</strong>: AMW Industrisanering <br>
Information concerning Älmby Entreprenad:<br>


<strong>Location</strong>: Älmhult<br>


<strong>Turn over</strong>: approximately €34M, of which €9M results from demolition<br>


<strong>Number of employees</strong>: 103<br>


<strong>Number of machine units</strong>: Approximately 30<br>


<strong>Additional info</strong>: The company was founded in 1998 and at that time mainly focused on general construction contracting. Five years ago it decided to expand into demolition and opened a demolition office in Helsingborg and Älmhult in Sweden. In 2018, turnover from demolition work was approximately €9M. Älmby Entreprenad has also acquired concrete cutting company Näsums Betonghåltagning to expand its operations in this field. Näsums has a turnover of approximately €2.3M and 15 staff.



</p>
