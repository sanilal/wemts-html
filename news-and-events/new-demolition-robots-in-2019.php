											<h4><a href="blog-details.html">New Demolition Robots in 2019</a></h4>
											<ul class="blog-meta">
												<li class="date"><i class="fa fa-calendar-check-o"></i> December 2019</li>
											</ul>
											<p>UK based ES Robotics has recently launched a series of brand new remote controlled demolition robots ranging from 1.5t to 8.5t, said to be the first in the world to have both diesel and electric power. According to the manufacturer, they follow many years of development followed by two years of field testing to get the new robots ready for market. The range consists of four models, DR125, DR300, DR450 and DR850, all marketed under the brand name ‘Akula’.


</p>
											<p>Further to being the world’s first dual power demolition robots, ES Robotics claims that the new models have several advantages over its competitors, especially regarding more reach and power, as well as the reduced weight of the bigger models. For example, the company says that its Akula DR125 is shorter than its similarly sized competitor Brokk 170 – both weigh 1,600kg – but has more reach and power. Its biggest model, the DR850, is nearly 2,500kg lighter than the Brokk 800 in either the diesel or electric version, but has the benefit of substantially more reach and dual power.



</p><h4>Brokk 70</h4>
											<p>Brokk has this year introduced the new Brokk 70. This new model is the said by the company to be the ideal solution for bringing safe, mechanised and efficient demolition to the most confined spaces. Compared to its predecessor, the Brokk 70 comes with innovative Brokk ‘SmartPower’ technology, as well as a powerful and fine-tuned hydraulic system. The innovative Brokk SmartPower electric powertrain increases power from 5.5kW to 9.8kW and enables the demolition robot to power twice the size breaker as its predecessor. Taken together, this leads to a 100% increase in demolition power over its predecessor, the Brokk 60.

 



</p>
											<p>The combination of Brokk SmartPower technology and Brokk’s upgraded hydraulic design has also led to both smoother and more precise movements, making the new robot smoother and faster to operate than ever before. Furthermore, the stronger hydraulics and completely redesigned mechanics of the machine enables it to carry and wield heavier and more powerful (many new) attachments. One such brand new attachment is the new Brokk BHB105 breaker which weighs in at over 100kg and comes with advanced features such as water spray to suppress dust and air cooling for work in a hot environment. Another new attachment is the Brokk BDC40 drum cutter which is said to bring real cutting power to the smallest segment of the demolition robot market for the first time, whereas  the new Brokk G32 demolition grapple is ideal for more efficient soft demolition.</p>
											<p>Despite all these new features and customer benefits, the Brokk 70 has the same extremely compact dimensions as its predecessor. The additional power and technology comes in a package that weighs only 560kg, fits through the narrowest doorways and can be transported in an ordinary passenger elevator. Finally and very importantly, it still runs on only a 16A fuse.

</p>



