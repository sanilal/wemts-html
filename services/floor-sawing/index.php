<h3>WHAT IS FLOOR SAWING</h3> 
<p>Floor sawing is the most efficient method to cut horizontally arranged concrete, reinforced concrete, asphalt, granite material. This application is achieved in combination with a drive unit propelled by an IC engine (Diesel or petrol), or electrical motor to rotate diamond circular blade against cutting arrangement to achieve required cuts.</p>
						 <h3>WHY DO YOU NEED FLOOR SAWING?</h3>
						 <p>Diamond floor sawing gives a very quick, clean straight cut. depth can be adjusted by increasing
the size of the blade, productivity can be fine-tuned using right power machine. This can be
utilized for engineering, maintenance, alteration, and demolition works.</p>
                         <h3>WHERE IS FLOOR SAWING REQUIRED?</h3>
                        <h4>Extensively used in</h4>
<p> <strong>Infra:</strong> roads, motor ways, tarmac areas, seaports, and airports.<br>
<strong>Civil:</strong> Parking, warehouses, floor slabs, Oil fields.</p>
						<h3>MACHINARIES REQUIRED</h3>
						 <p><strong>Floor saws:</strong> Rating from 10hp to 136hp<br>
<strong>Diamond Blades:</strong> 300mm to 1500mm (variable thickness)<br>
<strong>Accessories:</strong> Blade guard, Vacuum, Seat, Step on-board, drill, milling attachments etc.</p>
