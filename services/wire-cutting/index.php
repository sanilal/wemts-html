						 <h2>What is concrete wire cutting? </h2>
                         <p>Concrete wire or cutting is made to cut all types of high reinforced concrete structure such as columns beams, whaler beams, drop beams, pillars, crane foundations, bridges. </p>
                         <h3>What for wire cutting or wire sawing is used for? </h3>
                         <p>Wire diamond is used to cut or saw any depth of highly reinforced concrete structure with different dimensions along with depth from 500mm up to 7000 to 8000mm  (0.5 up to 7/8 meters) in one cut due to non-vibration system. </p>
                         <h3>Where do you use diamond wire cutting? </h3>
						 <p>Any applications  where depth is above 500mm wire sawing is used instead of blade cutting, those type of applications are encountered in the construction industries, marines, substations, airports oil & gas, etc.
</p>
<img src="../../images/services/wire-cutting.jpg" alt="Wire Cutting">
<p></p>
<h3>How fast is the wire diamond sawing? </h3>
<p>Its varies, all depending on the concrete component and tension in addition to how many layers and size of rebar inside the structure, however the average is between 5 to 7 square meter of cutting a day, nevertheless all depends on the application itself and the number of wire cutting machines that are required to complete the job. </p>
<h3>Can the diamond wire cut vertically or horizontally? </h3>
<p>Vertical and horizontal both ways and more, wire concrete cutting can saw cut in all sort of directions using the necessary spare parts and accessories. </p>
<h3>Is dry cutting possible using the diamond wire. </h3>
<p>Dry cutting is possible, the wire can execute 3 types of cutting , first is the reinforced concrete, steel wire cutting and 3rd is the dry cutting.</p>
<h3>Who is choosing which wire is required </h3>
<p> Our technical and engineering team will value the application after the job inspection to define which type of wire diamond is required. </p>
<h3>Is water required for wire cutting?</h3>
<p>Yes water is crucial for the cutting process with the diamond wire, therefore each cutting machine needs to be connected with an on going water with 2 to 4 bars pressure. </p>
<h3>Who has the biggest fleet of wire cutting machines </h3>
<p>WEM technical services is the main player having the biggest fleet of wire and sawing machines in the UAE and the gulf.</p>

