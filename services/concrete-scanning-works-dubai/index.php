						 <h3>Equipment used</h3>
						 <p>Using our PS 1000-B X-Scan Radar Detection System from Hilti takes our nondestructive inspection of concrete structures to a new level. It is designed to produce easy-to-read images of what’s inside concrete structures within minutes. Locating rebar, post tension cable, metal and plastic conduits, glass-fiber cables, voids and wood in dry concrete are made simple with the PS 1000-B X-Scan. Immediate scan analysis is possible as the objects are shown in a 2D plan view or cross sectional view directly on the PS 1000-B display.</p>
